// Arithmetic Operators
let a = 10;
let b = 3;

console.log(a + b); // Addition
console.log(a - b); // Subtraction
console.log(a * b); // Multipicaiton
console.log(a / b); // Division
console.log(a % b); // Model or Moduel something like that. But it's 1
console.log(a ** b); // This is like an exponent. So 10 times itself 3 times.

// Comparison Operators
console.log(a > b); // Greater than
console.log(a < b); // Less than
console.log(a == b); // checks if equal to
console.log(a != b); // not equal
console.log(a === b); // checks equal value and equal type

// Logical Operators
let x = true;
let y = false;

console.log(x && y); // AND (only returns true if both sides are true)
console.log(x || y); // OR (returns if at least one side is true)
console.log(!x); // NOT (flips the boolean value. So if it's true then it would switch it to false)
// If statement
let score = 90;

if (score >= 70) {
    console.log("You passed!");
}

// If else statements
let isHungry = false;

if (isHungry) {
    console.log("Let's eat!");
} else {
    console.log("Maybe later.");
}

// Else if statements
let grade = 82;

if (grade > 90) {
    console.log("A")
} else if (grade >= 80) {
    console.log("B")
} else if (grade >= 70) {
    console.log("C")
} else {
    console.log("F")
}

// Comparison + Logical Operators
let age = 17;
let hasPermission = true;

if (age >= 18 || hasPermission) {
    console.log("You can enter.");
} else {
    console.log("You cannot enter.")
}